// JavaScript Exercise #5 : Arrow Functions

// Convert the add function to an arrow function
const add = (a, b) => a + b;

// Testing the arrow function with different arguments
console.log(add(8, 12)); // Output: 20
console.log(add(-5, 15)); // Output: 10
console.log(add(0, -10)); // Output: -10
console.log(add(123, 456)); // Output: 579