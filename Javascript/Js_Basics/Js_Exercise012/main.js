//Write a for loop that logs each item in the array "apple", "banana", "cherry" and more.


const fruits = ["apple", "banana", "cherry", "orange", "mango"];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

console.log("number of fruits: " + fruits.length); // Output: number of fruits: 5
